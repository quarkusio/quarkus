    @Test
    public void multiCategoryGuideAppearsInBoth() throws Exception {
        NavigationConfig config = loadConfig("""
                categories:
                  - category: web
                    cat-title: Web
                  - category: data
                    cat-title: Data
                """);

        // Guide with two categories
        YamlMetadataGenerator.DocMetadata doc = createDoc(
                "REST with DB", "rest-db.adoc", "web, data", null, "REST with database", config);

        Map<String, YamlMetadataGenerator.DocMetadata> docs = new LinkedHashMap<>();
        docs.put(doc.filename, doc);

        Map<String, Object> root = generateNavigation(config, docs);
        List<Map<String, Object>> nav = getNavigation(root);

        assertEquals(2, nav.size());

        // Guide should appear in both categories
        for (Map<String, Object> category : nav) {
            List<Object> guides = getGuides(category);
            assertEquals(1, guides.size());
            @SuppressWarnings("unchecked")
            Map<String, Object> guide = (Map<String, Object>) guides.get(0);
            assertEquals("/guides/rest-db", guide.get("url"));
        }
    }
