    @SuppressWarnings("unchecked")
    @Test
    public void learningPathOutput() throws Exception {
        NavigationConfig config = loadConfig("""
                categories:
                  - category: web
                    cat-title: Web
                  - category: data
                    cat-title: Data

                learning-paths:
                  - path: rest-crud
                    path-title: Build a RESTful CRUD application
                    path-summary: Build a complete REST API with database access.
                    guides:
                      - hibernate-orm-panache.adoc
                      - rest.adoc
                """);

        YamlMetadataGenerator.DocMetadata restDoc = createDoc(
                "REST Guide", "rest.adoc", "web", null, "REST APIs", config);
        YamlMetadataGenerator.DocMetadata panacheDoc = createDoc(
                "Panache Guide", "hibernate-orm-panache.adoc", "data", null, "Panache", config);

        Map<String, YamlMetadataGenerator.DocMetadata> docs = new LinkedHashMap<>();
        docs.put(restDoc.filename, restDoc);
        docs.put(panacheDoc.filename, panacheDoc);

        Map<String, Object> root = generateNavigation(config, docs);

        // Verify learning-paths section exists
        assertTrue(root.containsKey("learning-paths"), "Should have learning-paths key");
        List<Map<String, Object>> paths = (List<Map<String, Object>>) root.get("learning-paths");
        assertEquals(1, paths.size());

        Map<String, Object> path = paths.get(0);
        assertEquals("rest-crud", path.get("path"));
        assertEquals("Build a RESTful CRUD application", path.get("path-title"));
        assertEquals("Build a complete REST API with database access.", path.get("path-summary"));

        // Guides should be in the config-specified order (panache first, rest second)
        List<Map<String, Object>> pathGuides = (List<Map<String, Object>>) path.get("guides");
        assertEquals(2, pathGuides.size());
        assertEquals("/guides/hibernate-orm-panache", pathGuides.get(0).get("url"));
        assertEquals("Panache", pathGuides.get(0).get("nav-title"));
        assertEquals("/guides/rest", pathGuides.get(1).get("url"));
        assertEquals("REST APIs", pathGuides.get(1).get("nav-title"));
    }

